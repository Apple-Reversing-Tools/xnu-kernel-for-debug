def get_register_name(register_tuple):
    """레지스터 튜플을 받아서 이름을 반환"""
    return REGISTER_NAMES.get(register_tuple, f"Unknown_Register_{register_tuple}")

def get_register_category(register_tuple):
    """레지스터 튜플을 받아서 카테고리를 반환"""
    for category, registers in REGISTER_CATEGORIES.items():
        if register_tuple in registers:
            return category
    return "Unknown"

def get_registers_by_category(category):
    """카테고리 이름을 받아서 해당 카테고리의 레지스터들을 반환"""
    return REGISTER_CATEGORIES.get(category, [])

def get_all_registers():
    """모든 레지스터 튜플을 반환"""
    return list(REGISTER_NAMES.keys())

def sys_reg_to_string(op0, op1, crn, crm, op2):
    """sys_reg 매개변수를 문자열로 변환"""
    return f"sys_reg({op0}, {op1}, {crn}, {crm}, {op2})"
